extern uint32_t 	                SystemCoreClock;





//---------------------------------------------------------------------------------------------
//                                        Functions
//---------------------------------------------------------------------------------------------
void DelayDWT_us(
                uint32_t us
                )
{
	DWT->CYCCNT = 0;
	uint32_t tick = us*(SystemCoreClock/1000000);
	while (DWT->CYCCNT < tick);
}




